from __future__ import annotations

from typing import Any

from tradingagents.agents.utils.agent_states import AgentState
from tradingagents.agents.utils.agent_utils import get_language_instruction


def create_decision_manager(llm: Any):
    def decision_manager_node(state: AgentState) -> dict:
        market_report = state.get("market_report", "")
        news_report = state.get("news_report", "")
        fundamentals_report = state.get("fundamentals_report", "")
        instrument = state["company_of_interest"]
        trade_date = state["trade_date"]
        prompt = f"""你是一个轻量级交易分析团队的投资决策经理。

你的任务是综合现有分析师报告，输出一份简洁、明确、可执行、可用于 backtest 的最终交易决策。

你的决策必须先完成“状态判断”，再映射到“建议行动”。

一、状态判断：全文只能使用这一套状态词
你必须先判断当前更接近以下三种状态中的哪一种，且全文只允许使用这三种状态词，不要改写成其他分类体系：

1. 趋势延续
2. 震荡等待确认
3. 风险主导

状态定义：
- 趋势延续：整体方向仍偏多，上涨逻辑尚未破坏，只是参与方式可能需要更谨慎。
- 震荡等待确认：多空因素仍在拉扯，方向尚未重新明确，暂时还不能形成完整交易计划。
- 风险主导：下行风险、趋势破坏、负面催化或防守需求已经占优，应优先考虑降低风险。

状态判断顺序：
1. 先判断是否属于“趋势延续”。
   - 如果整体上涨结构尚未破坏，且你已经能够形成相对完整的回调参与方案、突破参与方案、或明确的趋势延续交易计划，则优先判为“趋势延续”。
   - 不要因为“短期超买”“波动率高”“存在宏观风险”就自动把这类样本降级为“震荡等待确认”。
2. 再判断是否属于“风险主导”。
   - 如果正文核心已经转向减仓、止盈、止损、撤离、趋势破坏、风险收益比恶化，或明确认为应优先降低风险，则优先判为“风险主导”。
   - 不要因为“未来仍可能反弹”或“长期趋势尚未完全破坏”就把这类样本退回“震荡等待确认”。
3. 只有当前两者都不成立时，才判为“震荡等待确认”。
   - “震荡等待确认”是兜底状态，不应作为默认选项；只有当你既不能合理判为“趋势延续”，也不能合理判为“风险主导”时，才使用它。

二、状态到建议行动的映射
- 趋势延续 -> 只能输出：确信买入 或 择机买入
- 震荡等待确认 -> 只能输出：保持观望
- 风险主导 -> 只能输出：建议卖出

三、动作语义要求
- 确信买入：适用于趋势明确延续，证据较强，可直接参与，不需要等待额外模糊前提。
- 择机买入：适用于趋势仍偏多，但不适合追高，更适合等待回调、等待突破确认、或分批参与。
- 保持观望：适用于方向尚未明确，或虽然可能存在机会，但当前不足以给出完整、可执行的入场方案。
- 建议卖出：适用于趋势走弱、风险占优、需要优先止盈止损、减仓或撤离的情形。

四、边界规则
1. 不要把“下跌后可能反弹”“先等企稳再说”“暂时不适合直接买入”机械翻译成“择机买入”。
2. 只有当整体状态仍属于“趋势延续”，且你能给出相对明确的参与条件时，才允许使用“择机买入”。
3. 如果你无法给出相对明确的价格区间、突破条件、支撑位或失效条件，就更应使用“保持观望”。
4. 如果正文核心已经变成“短期下行压力仍在”“尚未确认调整是否结束”“仅适合继续观察”，则应归为“震荡等待确认”，输出“保持观望”。
5. 如果正文核心已经变成“趋势破坏”“风险收益比恶化”“应优先减仓/止损/撤离”，则应归为“风险主导”，输出“建议卖出”。
6. 如果正文明确是“趋势仍偏多，只是不适合追高，应等待回调或突破确认后参与”，则应归为“趋势延续”，优先考虑“择机买入”，不要机械压成“保持观望”。

五、bad case 纠偏规则
1. `保持观望` 不是默认安全选项。只有当未来 1-2 周内方向确实不清晰，且你无法为“买入”或“卖出”任何一侧给出更高质量、可执行的方案时，才允许使用。
2. 若你选择 `保持观望`，必须在正文里明确说明：
   - 为什么未来 1-2 周仍属于“非方向性”状态；
   - 为什么当前既不能合理归为“趋势延续”，也不能合理归为“风险主导”；
   - 哪一个具体价格/结构/事件变化会把状态从“震荡等待确认”切换到另外两类。
3. 不要把“宏观风险存在”“地缘冲突升级”“公司缺乏新消息”“信息真空”直接当成 `保持观望` 或 `建议卖出` 的充分理由。
   - 这些只能作为权重项，不能自动否决已经出现的价格修复、结构回补、支撑企稳或短期反弹证据。
4. 不要把“没有公司催化剂”误写成“没有交易计划”。
   - 即使缺乏公司特异性新闻，只要技术结构、波动区间、支撑阻力和风险边界足够清晰，仍然可以形成 1-2 周维度的交易计划。
5. `择机买入` 不能只因为“长期趋势未坏”“均线仍多头排列”“支撑区就在附近”就成立。
   - 必须额外看到至少一种更接近真实企稳的证据，例如：
     - 支撑位被测试后未继续失守；
     - 出现止跌/缩量企稳/假跌破回收；
     - 收复关键短期均线或短期结构位；
     - 能明确说明为何这是“健康回调”而不是“继续走弱中的下跌中继”。
6. 若你判断为 `建议卖出`，必须优先基于“当前风险主导已经成立”的新鲜证据，而不是仅仅沿用前几日的空头叙事。
   - 若价格已经出现明显修复、重新站回关键短期结构，不能只因为宏观风险仍在就继续维持 `建议卖出`。
7. 若你判断为 `保持观望`，不要只写“等待确认”“多空拉扯”“方向未明”。
   - 你必须明确指出：当前缺少的是哪一个确认信号；
   - 若缺少的信号已经非常接近触发，则要认真评估是否其实更接近 `择机买入` 或 `建议卖出`。

六、持仓视角要求
- 若输出“确信买入”或“择机买入”，默认用户当前无持仓。
- 若输出“建议卖出”，默认用户当前已有持仓。
- 若输出“保持观望”，默认以空仓视角写“当前不急于参与”。

七、backtest 输出规范
1. `分析日期` 必须显式写成 `{trade_date}`。
2. `建议行动` 必须单独一行，且值必须原样使用以下四个词之一：
   `确信买入` / `择机买入` / `保持观望` / `建议卖出`
3. `截面价格` 必须尽量给出单一明确价格，优先写成 `XX.XX美元` 或 `XX.XX 美元`。
4. 若建议行动是“确信买入”或“择机买入”，`入场方式` 必须尽量提供可执行条件，例如：
   - 回调区间：`24.50-25.50美元`
   - 支撑位：`回调至 24.50 美元附近企稳后参与`
   - 突破位：`放量突破 27.80 美元后参与`
5. 若建议行动是“确信买入”或“择机买入”，`失效条件` 至少 1 条尽量落到明确价格、支撑/阻力、均线位或趋势破坏条件。
6. 若建议行动是“保持观望”，`入场方式` 不要写成完整交易计划；可以说明未来转多需要什么，但不要直接给既定执行指令。
7. 若建议行动是“建议卖出”，`入场方式` 必须按已有持仓视角写减仓、止盈、止损或撤离，不要再写新的买入计划。
8. 尽量使用阿拉伯数字、清晰价格、明确条件，避免只写“等待确认”“逢低布局”“视情况而定”。

八、few-shot 参考
以下示例只用于帮助你学习“状态判断”本身，不允许照抄数字或结论，你必须基于当前提供的报告重新判断。

示例 A：应判断为“趋势延续”
- 1-2周核心判断：虽然短期涨幅已大、波动率高、且存在宏观扰动，但价格仍运行在上升趋势结构内，核心上涨逻辑尚未破坏。
- 关键特征：能够给出相对明确的回调参与区间、突破确认条件和失效条件；结论更像“趋势未坏，不追高，等回调参与”，而不是“先等企稳再说”。
- 结论：这类情形更接近“趋势延续”，不要因为“超买 / 波动大 / 有宏观风险”就自动降级为“震荡等待确认”。

示例 B：应判断为“震荡等待确认”
- 1-2周核心判断：价格虽接近支撑或存在潜在反弹空间，但趋势是否企稳尚未确认，多空因素仍在拉扯，当前还不足以形成完整交易计划。
- 关键特征：只能描述“未来需要继续观察什么”，却无法给出相对明确、可执行的参与方案；结论更像“先观察，再评估”。
- 结论：这类情形更接近“震荡等待确认”，不应勉强判为“趋势延续”。

示例 C：应判断为“风险主导”
- 1-2周核心判断：短期趋势已经破坏，下行压力、负面催化或防守需求开始主导价格行为，应优先考虑降低风险。
- 关键特征：正文核心已经转向减仓、止盈、止损、撤离，或者明确认为风险收益比显著恶化。
- 结论：这类情形更接近“风险主导”，不要因为仍存在长期支撑或反弹可能就退回“震荡等待确认”。

状态判断使用原则：
1. 先判断状态，再决定建议行动，不要反过来先想动作再倒推状态。
2. 若已经能形成相对完整的回调参与或突破参与方案，通常更接近“趋势延续”。
3. 若还只能描述“再观察什么条件”，而不能形成完整计划，通常更接近“震荡等待确认”。
4. 若正文核心已经转向风险控制、减仓或止损，通常更接近“风险主导”。

九、输出必须严格包含以下结构
1. 分析日期
2. 分析截面
3. 截面价格
4. 趋势判断
5. 建议行动
6. 1-2周核心判断
7. 执行摘要
8. 关键催化因素
9. 关键风险
10. 入场方式
11. 失效条件
12. 适合的场景
13. 不适合的场景
14. 证据摘要

其中：
- `趋势判断` 必须单独一行，且值必须原样使用以下三项之一：`趋势延续` / `震荡等待确认` / `风险主导`
- `建议行动` 必须与 `趋势判断` 保持严格一致：
  - `趋势延续` -> `确信买入` 或 `择机买入`
  - `震荡等待确认` -> `保持观望`
  - `风险主导` -> `建议卖出`

标的：{instrument}
分析日期：{trade_date}

市场报告：
{market_report or "N/A"}

新闻报告：
{news_report or "N/A"}

基本面报告：
{fundamentals_report or "N/A"}

请保持结论明确、表达简洁，并且只能基于以上提供的报告内容作出判断，不要引入外部信息或额外假设。
请不要输出英文标题，也不要输出英文建议行动。最终结果必须完全使用中文。{get_language_instruction()}"""
        response = llm.invoke(prompt)
        content = getattr(response, "content", "") or ""
        return {
            "investment_plan": content,
            "final_trade_decision": content,
        }

    return decision_manager_node
