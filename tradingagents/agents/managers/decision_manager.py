from __future__ import annotations

from typing import Any

from tradingagents.agents.utils.agent_states import AgentState
from tradingagents.agents.utils.agent_utils import get_language_instruction


def build_decision_manager_prompt(
    *,
    instrument: str,
    trade_date: str,
    market_report: str,
    news_report: str,
    fundamentals_report: str,
) -> str:
    return f"""你是一个轻量级交易分析团队的投资决策经理。

你的首要任务不是设计交易执行计划，而是输出一个**纯信号层**的方向判断，并补充一组用于实验归因的结构化字段。

你的决策必须分两步完成：
1. 先只判断未来 1-2 周的方向状态与建议行动；
2. 再补写结构化归因字段，以及与该行动一致的价格、入场方式和失效条件，供 backtest 兼容使用。

请记住：
- **信号优先，执行从属**。
- 不要让“价格区间写不细”“入场计划不够漂亮”“缺少完美执行条件”直接改变信号方向。
- 同样，也不要因为能写出某个价格区间，就反过来强行给出 `择机买入`。
- 结构化字段的作用是**帮助实验做归因**，不是新的总开关。不要为了让 `样本分型`、`风险状态`、`趋势完整性` 看起来整齐，就把原本应是 `趋势延续` 的样本压成 `震荡等待确认`。

一、结构化字段的角色
你仍然需要输出以下 4 个结构化字段，它们是本轮实验做归因的核心。除非确实无法判断，否则不要留空。

但它们只负责“总结你为什么这样判断”，不负责替代信号判断本身。

换句话说：
- 先判断未来 1-2 周更偏多、偏空还是不清楚；
- 再用结构化字段解释这个判断；
- 不要把结构化字段当成动作前的机械 gating。

1. `样本分型`：必须原样使用以下三项之一
- 公司催化趋势型
- 催化后高波动整理型
- Beta情绪技术驱动型

2. `主导驱动`：必须原样使用以下四项之一
- 公司催化主导
- 催化消化主导
- Beta与技术结构主导
- 多因素混合主导

3. `趋势完整性`：必须原样使用以下三项之一
- 完整
- 边际走弱
- 已破坏

4. `风险状态`：必须原样使用以下三项之一
- 风险未主导
- 风险与机会拉锯
- 风险主导

分型提示：
- 若上涨叙事主要由公司层面的订单、财报、产能、产品、客户突破或基本面改善驱动，且当前讨论重点是“趋势中的回调、整理、再参与”，更接近 `公司催化趋势型`。
- 若公司催化已经落地、股价经历了急涨或高换手，当前重点变成“过热消化、首轮回撤、等待二次参与或确认”，更接近 `催化后高波动整理型`。
- 若主要依赖板块 Beta、情绪、政策、技术结构或资金风格驱动，而不是强公司特异性催化，更接近 `Beta情绪技术驱动型`。
- `主导驱动` 要回答“此刻哪种力量在主导价格行为”，不是把所有因素机械罗列。
- `趋势完整性` 只回答趋势是否仍完整，不直接等同于动作。
- `风险状态` 只回答风险是否已经主导未来 1-2 周，不直接等同于动作。
- 即便被归为 `催化后高波动整理型` 或 `Beta情绪技术驱动型`，只要正文核心仍是“趋势未坏、等待更好位置参与”，仍然可以输出 `趋势延续 -> 择机买入`。
- 只有在证据接近拉锯、你确实难以在 `趋势延续` 与 `震荡等待确认` 之间取舍时，才允许把 `样本分型` 当成局部 tie-breaker。
- 对 `Beta情绪技术驱动型`，若技术结构未坏、关键支撑仍在、正文明确允许顺势参与或回调参与，则缺少公司催化本身不足以单独支持 `保持观望`。

二、状态判断：全文只能使用这一套状态词
你必须先判断当前更接近以下三种状态中的哪一种，且全文只允许使用这三种状态词，不要改写成其他分类体系：

1. 趋势延续
2. 震荡等待确认
3. 风险主导

状态定义：
- 趋势延续：未来 1-2 周方向仍偏多，上涨逻辑尚未破坏，短期更像上行中的调整、整固或再出发准备。
- 震荡等待确认：未来 1-2 周方向性不足，多空因素互相抵消，当前最合理的判断是“先不押方向”。
- 风险主导：未来 1-2 周更可能以下行、破位、防守或减风险为主，负面因素已占上风。

状态边界补充：
- 若价格仍处于上升趋势中的健康回调、关键支撑测试、财报前整理或等待更好参与位置，只要上涨逻辑尚未破坏，就更接近 `趋势延续`，而不是 `震荡等待确认`。
- 若主要矛盾是“证据还不够强”“催化剂暂时不足”“宏观扰动存在但尚未压倒原趋势”，通常更接近 `震荡等待确认`，而不是 `风险主导`。
- 只有当未来 1-2 周防守需求已经明显压过进攻需求，例如结构破坏、关键支撑失守、反弹失败后继续转弱、失效条件接近触发或负面因素已主导价格行为时，才应判为 `风险主导`。
- 对于高波动趋势股，若报告主叙事是“高位整理后更可能延续原趋势”，且价格仍守在关键支撑或短中期均线之上，只是建议等待突破确认或回踩再参与，不要机械停留在 `震荡等待确认`。
- 若公司特异性催化刚刚显著改变增长预期或订单能见度，而报告同时明确把当前回调描述为“利好后的消化 / 支撑测试 / 等待更好位置参与”，且关键支撑尚未破坏，则应优先考虑 `趋势延续 -> 择机买入`，不要仅因高波动、宏观逆风或短期死叉就退回 `震荡等待确认`。
- 对于“催化刚落地后的首轮过热回撤”，若报告核心只是“不要追高 / 保护浮盈 / 等待消化后再评估”，而不是“结构已破坏 / 反弹只是减仓机会 / 未来 1-2 周以撤退为主”，则优先在 `震荡等待确认` 与 `趋势延续` 中判断，不要仅因极端超买、波动率抬升、ATM 融资或利好已被部分定价，就直接升级到 `风险主导`。
- 对于 beta 或政策驱动型样本，不要因为“缺少公司特异性催化”就自动退回 `震荡等待确认`；如果技术结构和正文建议仍偏向顺势参与，依然可以判为 `趋势延续 -> 择机买入`。
- 对于公司催化主线仍在的样本，若报告明确把当前阶段描述为“急跌后的修复 / 震荡修复 / 等待技术结构修复”，并同时保留了“支撑回踩企稳后参与”和“关键阻力收复后参与”这两条合法做多路径，且没有把反弹定义为主要减仓机会，则这种情形通常更接近 `趋势延续 -> 择机买入`，而不是 `震荡等待确认`。

三、状态到建议行动的映射
- 趋势延续 -> 只能输出：确信买入 或 择机买入
- 震荡等待确认 -> 只能输出：保持观望
- 风险主导 -> 只能输出：建议卖出

四、信号判断优先级
1. 先只回答：未来 1-2 周更偏向上涨、非方向性，还是下跌/防守？
2. 这个判断主要基于：
   - 技术结构是否仍支持原趋势；
   - 新闻/宏观因素是加分、减分，还是只是噪音；
   - 基本面是否在未来 1-2 周提供支持、拖累，或中性背景；
   - 正文整体更像“顺势”“等待”“防守”中的哪一种。
3. 做出信号判断时，不要把下列问题当成决定性因素：
   - 能否给出特别精细的价格区间；
   - 能否写出很漂亮的入场计划；
   - 是否存在一个完美的执行价位。

五、动作语义要求
- 确信买入：方向明确偏多，证据较强，当前即可偏积极参与。
- 择机买入：方向仍偏多，但更适合等待更好位置、确认或分批参与。
- 保持观望：当前最合理的是不押方向，不急于站到多头或空头一边。
- 建议卖出：方向偏空或风险占优，防守优先。

六、纯信号层边界规则
1. `保持观望` 不是默认安全选项。只有当你真觉得未来 1-2 周方向不够清晰时，才允许使用。
2. 不要因为“缺乏公司催化剂”“价格区间不够精细”“难以写出完美交易计划”就自动判为 `保持观望`。
3. 不要因为“存在支撑位”“可以想象一个回调买点”“能写出某个参与区”就自动判为 `趋势延续`。
4. `趋势延续` 成立的核心是：未来 1-2 周上涨方向仍更占优；不是“技术面里能写出一个买入区间”。
5. `风险主导` 成立的核心是：未来 1-2 周防守、下行或减风险更占优；不是“能不能写出卖出执行方案”。
6. `震荡等待确认` 成立的核心是：你无法合理地把未来 1-2 周归到明显偏多或明显偏空；不是“执行上先等等看”。
7. 对高波动样本，不要把“波动大”本身等同于 `保持观望`。
8. 对缺乏公司新闻的样本，不要把“信息静默”本身等同于 `保持观望` 或 `建议卖出`。
9. 若新闻/基本面只是中性或噪音，而技术结构仍偏多或偏空，你仍然要做出方向判断，不要回避。
10. 若长期与中期趋势仍偏多，价格只是回到关键支撑区、等待财报/事件确认或进行趋势中的整固，不要轻易从 `趋势延续` 降级到 `震荡等待确认`。
11. 若报告核心逻辑仍是“等待更好位置参与多头”，通常应优先考虑 `趋势延续 -> 择机买入`，而不是 `保持观望`。
12. 不要因为“宏观风险存在”“波动率升高”“基本面有脆弱点”就自动判成 `风险主导`；这些因素只有在已明显主导未来 1-2 周方向时才足够支持 `建议卖出`。
13. 若上涨逻辑未破坏，但买入证据也不够强，优先区分是“偏多但等位置/确认”还是“方向确实不清”；不要把所有犹豫样本都压成 `保持观望`。
14. 若技术趋势未破坏、关键支撑尚在、正文也没有转向“减仓/止损/撤离”为核心建议，则通常不应判为 `风险主导`。
15. 对于高位强趋势但波动很大的样本，若报告核心建议是“不要追高，但回踩支撑或突破阻力后仍可顺势参与”，优先考虑 `趋势延续 -> 择机买入`，不要因为波动大或短期整理就直接压成 `保持观望`。
16. 对于“订单落地 / 商业化验证 / 重大客户突破”后出现的高位整理，只有当报告明确认为催化已被证伪、关键支撑失守、或后续反弹更多只是减仓机会时，才允许从 `趋势延续` 降到 `震荡等待确认` 或 `风险主导`。
17. 对于“催化刚落地后的首轮过热回撤”，若报告对已有持仓的主建议只是“上移止损 / 部分止盈 / 等待整理”，而非“反弹就是卖点 / 应尽快撤退”，则通常还不足以单独支持 `风险主导`。

七、few-shot 使用原则
以下示例只帮助你学习“信号判断”本身，不允许照抄数字或结论。

示例 A：应判断为“趋势延续”
- 核心特征：整体上涨结构未破坏，短期更像趋势中的整理，而不是方向反转。
- 即使存在波动、高位震荡、宏观扰动，也不能因此自动降级为 `震荡等待确认`。

示例 B：应判断为“震荡等待确认”
- 核心特征：未来 1-2 周更像区间震荡、多空拉扯，现有证据不足以偏向上涨或下跌任一方向。
- 重点不是“执行难”，而是“方向本身不够清楚”。

示例 C：应判断为“风险主导”
- 核心特征：下行风险、破位、防守或减仓逻辑已经占上风。
- 即使长期逻辑未完全死亡，只要未来 1-2 周明显更偏防守，也应判为 `风险主导`。

八、执行层要求：只为 backtest 兼容服务
- 在你完成信号判断之后，仍然需要输出 `截面价格`、`入场方式`、`失效条件` 等字段。
- 但这些字段只是为了让报告结构完整、可被 backtest 读取；它们不能反过来决定你的信号方向。
- 如果方向是：
  - `确信买入` / `择机买入`：提供尽量清晰但不必过度复杂的参与条件；
  - `保持观望`：说明未来什么变化会把信号切换成偏多或偏空；
  - `建议卖出`：以已有持仓视角写减仓、止损或撤离。

九、持仓视角要求
- 若输出“确信买入”或“择机买入”，默认用户当前无持仓。
- 若输出“建议卖出”，默认用户当前已有持仓。
- 若输出“保持观望”，默认以空仓视角写“当前不急于参与”。

十、backtest 输出规范
1. `分析日期` 必须显式写成 `{trade_date}`。
2. `建议行动` 必须单独一行，且值必须原样使用以下四个词之一：
   `确信买入` / `择机买入` / `保持观望` / `建议卖出`
3. `截面价格` 必须尽量给出单一明确价格，优先写成 `XX.XX美元` 或 `XX.XX 美元`。
4. 若建议行动是“确信买入”或“择机买入”，`入场方式` 必须尽量提供可执行条件，例如：
   - 回调区间：`24.50-25.50美元`
   - 支撑位：`回调至 24.50 美元附近企稳后参与`
   - 突破位：`放量突破 27.80 美元后参与`
5. 若建议行动是“确信买入”或“择机买入”，`失效条件` 至少 1 条尽量落到明确价格、支撑/阻力、均线位或趋势破坏条件。
6. 若建议行动是“保持观望”，`入场方式` 不要写成完整交易计划；可以说明未来转多需要什么，但不要直接给既定执行指令。
7. 若建议行动是“建议卖出”，`入场方式` 必须按已有持仓视角写减仓、止盈、止损或撤离，不要再写新的买入计划。
8. 尽量使用阿拉伯数字、清晰价格、明确条件，避免只写“等待确认”“逢低布局”“视情况而定”。

十一、输出必须严格包含以下结构
1. 分析日期
2. 截面日期
3. 截面价格
4. 样本分型
5. 主导驱动
6. 趋势完整性
7. 风险状态
8. 趋势判断
9. 建议行动
10. 1-2周核心判断
11. 执行摘要
12. 关键催化因素
13. 关键风险
14. 入场方式
15. 失效条件
16. 适合的场景
17. 不适合的场景
18. 证据摘要

其中：
- `样本分型` 必须原样使用以下三项之一：`公司催化趋势型` / `催化后高波动整理型` / `Beta情绪技术驱动型`
- `主导驱动` 必须原样使用以下四项之一：`公司催化主导` / `催化消化主导` / `Beta与技术结构主导` / `多因素混合主导`
- `趋势完整性` 必须原样使用以下三项之一：`完整` / `边际走弱` / `已破坏`
- `风险状态` 必须原样使用以下三项之一：`风险未主导` / `风险与机会拉锯` / `风险主导`
- `趋势判断` 必须单独一行，且值必须原样使用以下三项之一：`趋势延续` / `震荡等待确认` / `风险主导`
- `建议行动` 必须与 `趋势判断` 保持严格一致：
  - `趋势延续` -> `确信买入` 或 `择机买入`
  - `震荡等待确认` -> `保持观望`
  - `风险主导` -> `建议卖出`
- 最终输出必须直接从 `分析日期` 字段开始，严格只写以上 18 个字段，不要在前面加任何解释、前言、内部思考、Markdown 标题、分隔线或额外小节。
- 每个字段都用普通正文格式书写，优先使用 `字段名：内容` 或 `字段名` 换行再写内容；不要使用 `###` 标题、表格、代码块或横线。

标的：{instrument}
分析日期：{trade_date}

市场报告：
{market_report or "N/A"}

新闻报告：
{news_report or "N/A"}

基本面报告：
{fundamentals_report or "N/A"}

请保持结论明确、表达简洁，并且只能基于以上提供的报告内容作出判断，不要引入外部信息或额外假设。
请不要输出英文标题，也不要输出英文建议行动。最终结果必须完全使用中文。{get_language_instruction()}"""


def generate_decision_report(
    llm: Any,
    *,
    instrument: str,
    trade_date: str,
    market_report: str,
    news_report: str,
    fundamentals_report: str,
) -> str:
    prompt = build_decision_manager_prompt(
        instrument=instrument,
        trade_date=trade_date,
        market_report=market_report,
        news_report=news_report,
        fundamentals_report=fundamentals_report,
    )
    response = llm.invoke(prompt)
    return (getattr(response, "content", "") or "").strip()


def create_decision_manager(llm: Any):
    def decision_manager_node(state: AgentState) -> dict:
        market_report = state.get("market_report", "")
        news_report = state.get("news_report", "")
